const month = "march"

switch (month) {
    case "january":
        console.log("January")
        break;
    case "february":
        console.log("February")
        break;
    case "march":
        console.log("March")
        break; // break - Break the control flow
    case "april":
        console.log("April")
        break;

    default:
        console.log("default case match");
        break;
}